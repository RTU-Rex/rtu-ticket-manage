  <h3>Monitor FAQs</h3>
  <div class="accordeon">
  <input type="checkbox" name="accordeon-title4" class="accordeon-checkbox" id="accordeon-title4" />
  <label for="accordeon-title4" class="accordeon-title-label">There is no display on the monitor, what do I do now? </label>
    <div class="accordeon-content">
      <p>Make sure the monitor is on. If no power light (green or orange light) is seen on the monitor display try pressing the power button until it comes on. If your computer monitor was on and you stepped away from the computer and upon returning it was black, it's likely that the computer is asleep. Try moving your mouse, clicking the mouse buttons, and/or pressing any key (space bar) on the keyboard to wake it up. Make sure that the monitor is connected properly to the back of the computer.</p>
  </div>
  </div>

  <div class="accordeon">
  <input type="checkbox" name="accordeon-title5" class="accordeon-checkbox" id="accordeon-title5" />
    <label for="accordeon-title5" class="accordeon-title-label">What do I do if the image on my monitor is distorted or skewed?</label>
    <div class="accordeon-content">
    <p>You may receive a distorted image when the cable is loose or defective. Disconnect the video cable going from the back of the computer and verify that no pins are bent, burnt or broken. Once verified re-connect the monitor cable. If the refresh rate is not properly set the monitor may have a wavy or an appearance that lines are going down or across the monitor slowly or fast, this may also cause a flickering affect. A distorted image can be caused by magnetic or other types of interference.</p>
  </div>
  </div>

